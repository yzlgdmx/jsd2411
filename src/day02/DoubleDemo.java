package day02;

/**
 * double类型的演示
 */
public class DoubleDemo {
    public static void main(String[] args) {
        Double.toString(3.14);
        double c = 3.0, d = 2.9;
        System.out.println(c - d);
        double e = 6.0, f = 4.9;
        System.out.println(e - f);
        double g = 6.0, h = 1.9;
        System.out.println(g - h);
    }
}
